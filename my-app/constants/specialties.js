// Helper function to get specialty by ID
export const getSpecialtyById = (id, language = 'bg') => {
  const specialty = specialties.find(s => s.id === id);
  if (!specialty) return null;
  return specialty[language] || specialty.bg;
};

// Helper function to get specialty ID by name (case-insensitive search)
export const getSpecialtyIdByName = (name, language = 'bg') => {
  const specialty = specialties.find(s => 
    s[language]?.toLowerCase() === name?.toLowerCase() ||
    s.bg?.toLowerCase() === name?.toLowerCase() ||
    s.en?.toLowerCase() === name?.toLowerCase() ||
    s.de?.toLowerCase() === name?.toLowerCase()
  );
  return specialty?.id || null;
};

// Helper function to get all specialties for a language
export const getSpecialtiesForLanguage = (language = 'bg') => {
  return specialties.map(s => ({
    id: s.id,
    name: s[language] || s.bg
  }));
};

